public class Quadri_Angles
{
    public static void main()
    {
        int a = 3;
        int b = 4;
        int c = 5; 
        int d = 6;
        
        double e = 360/(a+b+c+d);
        
        System.out.println("Angle A = "+ 3*e);
        System.out.println("Angle B = "+ 4*e);
        System.out.println("Angle C = "+ 5*e);
        System.out.println("Angle A = "+ 6*e);
    }
}